# Easy Numpy Arrays

import numpy as np

array_zero = np.zeros((2,2))        # 2x2 matrix, all value is 0
print(array_zero)

array_ones = np.ones((2,3))         # 2x3 matrix, all value is 1
print(array_ones)

array_full = np.full((2,3), 5)      # 2x3 matrix, all value is 5
print(array_full)